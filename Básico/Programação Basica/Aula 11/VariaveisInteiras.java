public class VariaveisInteiras{
    public static void main(String[] args){
        
        byte idade01 = 20;

        short idade02 = 21;

        int idade03 = 22;

        long idade04 = 04;

        System.out.println("byte => idade01 - " + idade01);
        System.out.println("short => idade02 - " + idade02);
        System.out.println("int => idade03 - "+ idade03);
        System.out.println("long => idade04 - " + idade04);
    }
}